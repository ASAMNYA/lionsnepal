                                <h5 class="text-center">LIONS Club International:</h5>
<p>Lions Club international has dominated the world of social service with a century long history of selfless, incorruptible, noble and magnanimous adherence to Society and community and succeeded to be the largest leading non-profitable and secular social organization.
Lions Clubs International, a service membership organization of over 1.4 million members worldwide (as of April 2015), was founded in Evansville, Indiana on 24 October 1916 by Dr. William Perry Woods and subsequently evolved as an international service organization under the guidance and supervision of its Secretary, Melvin Jones.
In 1917, Melvin Jones, was a 38-year-old Chicago business leader who told members of his local business club they should reach beyond business issues and address the betterment of their communities and the world. Jones' group, the Business Circle of Chicago, agreed. After contacting similar groups around the United States, an organizational meeting was held on June 7, 1917, in Chicago, Illinois, USA. The Business Circle subsequently joined one of the invited groups, the "International Association of Lions Clubs"</p>

